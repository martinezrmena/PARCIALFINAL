package usonsonate.com.tukybirth.Galeria;

public class IMG {

    String id;
    String nombreImg;

    public IMG(String id, String nombreImg) {
        this.id = id;
        this.nombreImg = nombreImg;
    }


    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getNombreImg() {
        return nombreImg;
    }

    public void setNombreImg(String nombreImg) {
        this.nombreImg = nombreImg;
    }


}
